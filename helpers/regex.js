export const regex = {
  mail: /^(([^<>()[\]\.,;:\s@\"]+(\.[^<>()[\]\.,;:\s@\"]+)*)|(\".+\"))@(([^<>()[\]\.,;:\s@\"]+\.)+[^<>()[\]\.,;:\s@\"]{2,})$/i,
  password: {
    min: /^.{8,}$/,
    upper: /^(?=.*[A-Z])/,
    lower: /^(?=.*[a-z])/,
    number: /^(?=.*\d)/,
    spaces: /^\S*$/,
    special: /^(?=.*[^a-zA-Z0-9\s])/,
  },
};
